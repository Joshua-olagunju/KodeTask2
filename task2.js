// Task 2: Notes on data types

// a. String data type
// Strings are primitive data types in JavaScript that represent a sequence of characters.
// They are immutable, meaning their values cannot be changed after creation.
// Strings can be created using single quotes, double quotes, or backticks (template literals).

// 3 methods for String:
// 1. length: Returns the number of characters in the string
// Example: "Hello".length; // Returns 5

// 2. toUpperCase(): Converts all characters in the string to uppercase
// Example: "hello".toUpperCase(); // Returns "HELLO"

// 3. substring(startIndex, endIndex): Extracts a portion of the string between the specified indices
// Example: "Hello World".substring(0, 5); // Returns "Hello"

// b. Number data type
// Numbers are primitive data types in JavaScript that represent numeric values.
// They can be integers or floating-point numbers.
// JavaScript uses 64-bit floating-point representation for all numbers.

// 3 methods for Number:
// 1. toFixed(digits): Formats the number to a string with a specified number of decimal places
// Example: (3.14159).toFixed(2); // Returns "3.14"

// 2. toString(): Converts the number to a string representation
// Example: (42).toString(); // Returns "42"

// 3. isNaN(): Checks if the value is NaN (Not-a-Number)
// Example: isNaN(42); // Returns false